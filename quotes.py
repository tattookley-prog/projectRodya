class QuotePool:
    """Пул цитат: объединяет предопределённые и добавленные пользователем цитаты."""

    def __init__(self):
        # Работаем с копией, чтобы не изменять оригинал
        self._quotes = list(PREDEFINED_QUOTES)

    def add_quote(self, text: str, author: str, topic: str) -> None:
        """Добавляет новую цитату в пул."""
        self._quotes.append({"text": text, "author": author, "topic": topic})

    def get_quotes(self, author: str = None, topic: str = None) -> list:
        """
        Возвращает список цитат с учётом фильтров.
        Если фильтр None или пустая строка — не применяется.
        """
        result = self._quotes
        if author:
            result = [q for q in result if q["author"] == author]
        if topic:
            result = [q for q in result if q["topic"] == topic]
        return result

    def get_random_quote(self, author: str = None, topic: str = None) -> dict | None:
        """
        Возвращает случайную цитату из отфильтрованного пула.
        Возвращает None, если подходящих цитат нет.
        """
        pool = self.get_quotes(author=author, topic=topic)
        if not pool:
            return None
        return random.choice(pool)

    def get_authors(self) -> list:
        """Возвращает отсортированный список уникальных авторов."""
        return sorted({q["author"] for q in self._quotes})

    def get_topics(self) -> list:
        """Возвращает отсортированный список уникальных тем."""
        return sorted({q["topic"] for q in self._quotes})
